SELECT name, population, ST_astext(location)
FROM city 
WHERE ST_Covers(
    ST_GeomFromText(
        'POLYGON((-14.1796875 46.76287574866777, -14.1796875 55.598883895854186, 27.5390625 55.598883895854186, 27.5390625 46.76287574866777, -14.1796875 46.76287574866777))'),
    st_setsrid(location::geometry,0))
ORDER BY name
LIMIT 100
